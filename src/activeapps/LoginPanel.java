    public void loadFromFile (String fileName) throws IOException{
    	BufferedReader reader = new BufferedReader(new FileReader(fileName));
    	String line = null;
    	while ((line = reader.readLine()) != null) {
    		ListUserPass.add(line);
    	}
        reader.close();
    }
