    private void jButtonLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonLoginMouseClicked
        String Pass = new String(Password.getPassword());
        String Name = new String(UserName.getText());
        Password.setText("");
        UserName.setText("");
        if(Pass.compareTo(DefPassword)== 0 &&
                Name.compareTo(DefUsername) == 0)
            MainFrame.SetNewPanel("PanitiaSession");
        else if (ListUserPass.contains(Name+" "+Pass))
            MainFrame.SetNewPanel("PesertaSession");
        else
            JOptionPane.showMessageDialog(this, "Maaf! Username atau Password Anda salah.", "Login error", JOptionPane.ERROR_MESSAGE);
        //System.out.println("sapiii "+UserName.getText().toString()+"  "+Pass);
    }//GEN-LAST:event_jButtonLoginMouseClicked

