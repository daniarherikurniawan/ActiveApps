    public void LoadTTSFromFile (String fileName) throws IOException{
    	BufferedReader reader = new BufferedReader(new FileReader(fileName));
    	String line = null;
    	while ((line = reader.readLine()) != null) {
            String parts[] = line.split(" ");
            SoalTTS Soal = new SoalTTS(Integer.parseInt(parts[1]),
                    Integer.parseInt(parts[2]),
                    parts[0].charAt(0),Integer.parseInt(parts[3]),
                    reader.readLine().trim());
            ListSoal.add(Soal);
    	}
        reader.close();
    }
    
    class CountdownTimerListener implements ActionListener{
        int timeRemaining ;
        
        public CountdownTimerListener(int Time){
            timeRemaining = Time;
        }
    
        @Override
        public void actionPerformed(ActionEvent e) {
            if (--timeRemaining > 0){
                repaintLabel(timeRemaining);
            }else{
                LabelTimer.setText("Time's up!");
                countdownTimer.stop();
            }
        }
    }
    
