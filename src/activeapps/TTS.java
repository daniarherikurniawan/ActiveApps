    public static void UpdateGambarTepi(int X, int Y){
        if (!Kolom[X][Y].IsNoSoalSama(0)&&!Kolom[X][Y].terjawab){
            try {
                if (Kolom[X][Y].IsTepiMerah){
                    Kolom[X][Y].IsTepiMerah = false;
                    Kolom[X][Y].image = ImageIO.read(new File("img/test/Border16.png"));
                }else{
                    Kolom[X][Y].image = ImageIO.read(new File("img/test/RedBorder16.png"));
                    Kolom[X][Y].IsTepiMerah = true;                    
                }
                 Kolom[X][Y].setPreferredSize(new Dimension(Kolom[X][Y].image.getWidth(),Kolom[X][Y].image.getHeight()));
                Kolom[X][Y].repaint();
            } catch (IOException ex) {
                Logger.getLogger(TTS.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

