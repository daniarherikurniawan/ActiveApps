    public boolean IsAngka(String Str){
        Str = Str.substring(0,Str.length()-1);
        if(Abjad.contains(Str)){
            return false;
        }else{
            return true;
        }
    }
    
    public void LoadSoalFromFile (String fileName) throws IOException{
    	BufferedReader reader = new BufferedReader(new FileReader(fileName));
    	String line = null;
    	while ((line = reader.readLine()) != null) {
            MatchingQuestion Soal = new MatchingQuestion(Integer.parseInt(line),
                    reader.readLine().toString(),reader.readLine().toString());
            ListSoal.add(Soal);
            SoalAcak.add(Soal);
        }
        Collections.shuffle(SoalAcak);
        Collections.shuffle(SoalAcak);
        reader.close();
    }
    
