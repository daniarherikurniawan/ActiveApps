    class CountdownTimerListener implements ActionListener{
        int timeRemaining ;
        
        public CountdownTimerListener(int Time){
            timeRemaining = Time;
        }
    
        @Override
        public void actionPerformed(ActionEvent e) {
            if (--timeRemaining > 0){
                repaintLabel(timeRemaining);
            }else{
                LabelTimer.setText("Time's up!");
                ShowScore();
                countdownTimer.stop();
            }
        }
    }
    
    private boolean IsDijawab(Component co[]){
            JLabel La = (JLabel) co[4];
            if(IsAngka(La.getText()))
                return true;
            else
                return false;
    }
    
