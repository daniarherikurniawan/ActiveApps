    private void ShowScore(){
        JOptionPane.showMessageDialog(this,"Score tim Anda adalah "+
                PesertaSessionPanel.Score+".","Score",JOptionPane.INFORMATION_MESSAGE);
        countdownTimer.stop();
        TimeRemaining = 0;
        ListSoal.clear();
        try {
            PesertaSessionPanel.TulisNilaiKeFile("NilaiPeserta.in");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Computerise.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(Computerise.class.getName()).log(Level.SEVERE, null, ex);
        }
        //currNo = 0;
        //bgAnswer.clearSelection();
        NextSoal.setEnabled(true);
        Selesai.setEnabled(false);
        PesertaSessionPanel.SudahMengerjakan = true;
        MainFrame.SetNewPanel("PesertaSession");
    }
    
    public void repaintLabel(int Second){
        if(IsHide){
            LabelTimer.setText("");
        }else{
            TimeRemaining = Second;
            int hh = Second/3600;
            int mm = (Second%3600)/60;
            int ss = (Second%3600)%60;
            String StrHH, StrMM, StrSS;
            if (hh < 10)
                StrHH = new String("0"+hh);
            else
                StrHH = new String(Integer.toString(hh));
            if (mm < 10)
                StrMM = new String("0"+mm);
            else
                StrMM = new String(Integer.toString(mm));
            if (ss < 10)
                StrSS = new String("0"+ss);
            else
                StrSS = new String(Integer.toString(ss));
            String Jam = new String(StrHH+":"+StrMM+":"+StrSS);

            LabelTimer.setText(Jam);
        }
    }
    
